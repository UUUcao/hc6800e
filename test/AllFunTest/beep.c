/* 蜂鸣器响一声                 */
/*******************************************************************/
#include "common.h"

void beep()
{
    unsigned int y = 10000 ;
    while( (y > 0) && is_quit())
    {
		y--;
      	delay_ms(20) ;
      	BEEP=!BEEP ;                //BEEP取反
    } 
    BEEP=1 ;                      //关闭蜂鸣器
}