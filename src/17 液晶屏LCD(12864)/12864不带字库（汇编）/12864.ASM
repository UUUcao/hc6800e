       WC:    MOV     A,VAR
                MOV     LCD_DATA,A
                CLR     DI            ;SET INSTRUCTION MODE
                CLR     RW            ;SET WRITE MODE
                SETB    CS1           ;SELECT CS1
                SETB    CS2           ;SELECT CS2
                SETB    E             ;ENABLE
                NOP
                CLR     E             ;DISABLE
                NOP
                RET

        WD1:    MOV     A,VAR
                MOV     LCD_DATA,A
                SETB    DI            ;SET DATA MODE
                CLR     RW            ;SET WRITE MODE
                SETB    CS1           ;SELECT CS1
                CLR     CS2           ;CANCEL CS2
                SETB    E             ;ENABLE
                NOP
                CLR     E             ;DISABLE
                NOP
                RET

        WD2:    MOV     A,VAR
                MOV     LCD_DATA,A
                SETB    DI           ;SET DATA MODE
                CLR     RW           ;SET WRITE MODE
                CLR     CS1          ;CANCEL CS1
                SETB    CS2          ;SELECT CS2
                SETB    E            ;ENABLE
                NOP
                CLR     E            ;DISABLE
                NOP
                RET

      RESET:    CLR     RST          ;SET RST GO HIGH
                CALL    DELAY
                SETB    RST
                CALL    DELAY
                RET

PAUSE:          JMP     PAUSE


